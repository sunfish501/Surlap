String _key(DateTime d) =>
    '${d.year}-${d.month.toString().padLeft(2, '0')}-${d.day.toString().padLeft(2, '0')}';

/// 그 날 공휴일 이름(없으면 null).
String? holidayName(DateTime d) => kKoreanHolidays[_key(d)];

/// 공휴일(빨간날) 여부.
bool isHoliday(DateTime d) => kKoreanHolidays.containsKey(_key(d));

/// 날짜키(YYYY-MM-DD)로 공휴일 이름.
String? holidayNameForKey(String dateKey) => kKoreanHolidays[dateKey];
